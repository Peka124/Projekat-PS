    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "obrisete ovu opremu?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                Komunikacija.getInstance().deleteOprema(o);
                FormPretragaOpreme fp = (FormPretragaOpreme) getParent();
                fp.refreshTable();
                fp.popuniOpremu();
                JOptionPane.showMessageDialog(this, "Uspesno obrisana oprema.");
                this.dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Ne mozete da obrisete ovu opremu "
                        + "jer je bilo naruceno!");
//                Logger.getLogger(FormDetaljiOpreme.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        try {

            if (txtNaziv.getText().isEmpty() || txtOpis.getText().isEmpty()
                    || txtCena.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            String naziv = txtNaziv.getText();
            String opis = txtOpis.getText();
            double cena = Double.parseDouble(txtCena.getText());

            o.setNazivOpreme(naziv);
            o.setCena(cena);
            o.setOpis(opis);
            
            Komunikacija.getInstance().updateOprema(o);
            FormPretragaOpreme fp = (FormPretragaOpreme) getParent();
            fp.refreshTable();
            fp.popuniOpremu();
            JOptionPane.showMessageDialog(this, "Uspesno izmenjena oprema.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
//            Logger.getLogger(FormDetaljiOpreme.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed


